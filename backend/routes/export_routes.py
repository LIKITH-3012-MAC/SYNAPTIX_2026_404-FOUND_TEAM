@router.get("/issues/csv")
async def export_issues_csv(
    status: Optional[str] = None,
    category: Optional[str] = None,
    current_user: dict = Depends(get_current_user)
):
    """Secure CSV Export for current user's issues."""
    query = "SELECT * FROM issues WHERE reporter_id = %s"
    params = [current_user["sub"]]
    
    if status:
        query += " AND status = %s"
        params.append(status)
    if category:
        query += " AND category = %s"
        params.append(category)
        
    query += " ORDER BY created_at DESC"
    
    with get_db() as cursor:
        cursor.execute(query, tuple(params))
        rows = cursor.fetchall()

    if not rows:
        raise HTTPException(status_code=404, detail="No reports found for export.")

    df = pd.DataFrame(rows)
    
    # Sanitize for export
    if "reporter_id" in df.columns: df.drop(columns=["reporter_id"], inplace=True)
    
    output = io.StringIO()
    df.to_csv(output, index=False)
    
    return Response(
        content=output.getvalue().encode('utf-8'),
        media_type="text/csv",
        headers={"Content-Disposition": f"attachment; filename=resolvit_reports_{datetime.now().strftime('%Y%m%d')}.csv"}
    )

@router.get("/issues/pdf")
async def export_issues_pdf(
    status: Optional[str] = None,
    category: Optional[str] = None,
    current_user: dict = Depends(get_current_user)
):
    """Secure Premium PDF Export for current user's issue history with filtering."""
    query = "SELECT * FROM issues WHERE reporter_id = %s"
    params = [current_user["sub"]]
    
    if status:
        query += " AND status = %s"
        params.append(status)
    if category:
        query += " AND category = %s"
        params.append(category)
        
    query += " ORDER BY created_at DESC"
    
    with get_db() as cursor:
        # Get User details for the PDF header
        cursor.execute("SELECT username, full_name, email FROM users WHERE id = %s", (current_user["sub"],))
        user_data = cursor.fetchone()
        
        # Get Issues
        cursor.execute(query, tuple(params))
        issues = cursor.fetchall()

    if not issues:
        raise HTTPException(status_code=404, detail="No reports found for export.")

    pdf_bytes = create_issue_pdf(issues, user_data)
    
    return Response(
        content=pdf_bytes,
        media_type="application/pdf",
        headers={"Content-Disposition": f"attachment; filename=resolvit_report_history_{datetime.now().strftime('%Y%m%d')}.pdf"}
    )

@router.get("/issue/{issue_id}/pdf")
async def export_single_issue_pdf(
    issue_id: str,
    current_user: dict = Depends(get_current_user)
):
    """Export a single issue as a 'Certificate of Submission' or Full Report."""
    with get_db() as cursor:
        cursor.execute("SELECT username, full_name, email FROM users WHERE id = %s", (current_user["sub"],))
        user_data = cursor.fetchone()
        
        cursor.execute("SELECT * FROM issues WHERE id = %s", (issue_id,))
        issue = cursor.fetchone()

    if not issue:
        raise HTTPException(status_code=404, detail="Issue not found.")
        
    # Security check: User can only download their own issue unless Admin
    if str(issue["reporter_id"]) != current_user["sub"] and current_user["role"] != "admin":
        raise HTTPException(status_code=403, detail="Unauthorized export request.")

    pdf_bytes = create_issue_pdf([issue], user_data)
    safe_id = str(issue_id)[:8]
    
    return Response(
        content=pdf_bytes,
        media_type="application/pdf",
        headers={"Content-Disposition": f"attachment; filename=resolvit_issue_{safe_id}.pdf"}
    )
