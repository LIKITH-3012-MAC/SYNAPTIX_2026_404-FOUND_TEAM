    setLanguage(lang) {
        if (!this.languages[lang]) return;
        this.currentLang = lang;
        localStorage.setItem('resolvit_lang', lang);
        document.documentElement.lang = lang;
        this.apply();
        
        // Dispatch event for components
        window.dispatchEvent(new CustomEvent('resolvit-lang-change', { detail: lang }));
        
        showToast(`🌐 Language set to ${this.languages[lang].name}`, 'info');
    },

